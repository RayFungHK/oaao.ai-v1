<?php

/**
 * GET /chat/workspace-panel — authenticated HTML fragment for the workspace shell mount (owned by chat module).
 *
 * Lives under {@code controller/panel/} so the closure path contains {@code /} and avoids the controller-root
 * `{className}.{name}.php` prefix rule (naming collision guard).
 */
return function (): void {
    header('Cache-Control: no-store, no-cache, must-revalidate');

    $auth = $this->api('auth');
    $user = ($auth && method_exists($auth, 'getUser')) ? $auth->getUser() : null;

    if (! $user) {
        http_response_code(401);
        header('Content-Type: text/html; charset=UTF-8');
        echo '<p class="p-lg text-sm fg-[var(--grid-ink-muted)]">Sign in required.</p>';

        return;
    }

    header('Content-Type: text/html; charset=UTF-8');

    echo $this->loadTemplate('workspace_panel')->output();
};
