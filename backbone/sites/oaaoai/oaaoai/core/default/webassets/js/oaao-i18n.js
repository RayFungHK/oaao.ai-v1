function mergeDeep(base, over) {
    const out = { ...base };
    for (const [lang, map] of Object.entries(over || {})) {
        if (!map || typeof map !== 'object') continue;
        out[lang] = { ...(out[lang] || {}), ...map };
    }
    return out;
}

/** @type {Record<string, Record<string, string>>} */
let merged = mergeDeep(
    OAAO_I18N_BASE,
    typeof globalThis.OAAO_I18N_OVERRIDES === 'object' && globalThis.OAAO_I18N_OVERRIDES !== null
        ? globalThis.OAAO_I18N_OVERRIDES
        : {},
);

/** Reload merges when overrides are injected late. */
export function oaaoI18nReloadOverrides() {
    merged = mergeDeep(
        OAAO_I18N_BASE,
        typeof globalThis.OAAO_I18N_OVERRIDES === 'object' && globalThis.OAAO_I18N_OVERRIDES !== null
            ? globalThis.OAAO_I18N_OVERRIDES
            : {},
    );
}

/** @returns {'en' | 'zh-Hant'} */
export function normalizeOaaoLocale(raw) {
    const s = String(raw ?? '').trim();
    if (s === 'zh-Hant' || s.toLowerCase() === 'zh-hant' || s.toLowerCase().startsWith('zh')) {
        return 'zh-Hant';
    }
    return 'en';
}

/**
 * Apply display locale for {@link oaaoT} and orchestrator locale hints (wizard, samples).
 *
 * @param {string} locale
 */
export function oaaoApplyUiLocale(locale) {
    if (typeof document === 'undefined') return;
    const lang = normalizeOaaoLocale(locale);
    document.documentElement.lang = lang;
    if (document.body) {
        document.body.dataset.oaaoUiLocale = lang;
    }
    document.dispatchEvent(new CustomEvent('oaao-locale-changed', { detail: { locale: lang } }));
}

/** @returns {'en' | 'zh-Hant'} */
export function oaaoResolveLang() {
    const fromBody =
        typeof document !== 'undefined' ? document.body?.dataset?.oaaoUiLocale?.trim() : '';
    if (fromBody) {
        return normalizeOaaoLocale(fromBody);
    }
    const raw = (document.documentElement.lang || navigator.language || 'en').toLowerCase();
    if (raw.startsWith('zh')) return 'zh-Hant';
    return 'en';
}

/**
 * @param {string} key dotted key
 * @param {string} [fallback]
 * @param {Record<string, string | number>} [vars] placeholders {{name}}
 */
export function oaaoT(key, fallback = '', vars = {}) {
    const lang = oaaoResolveLang();
    const pick =
        merged[lang]?.[key] ?? merged.en?.[key] ?? (fallback !== '' ? fallback : key);
    let out = pick;
    for (const [vk, vv] of Object.entries(vars)) {
        out = out.split(`{{${vk}}}`).join(String(vv));
    }
    return out;
}
